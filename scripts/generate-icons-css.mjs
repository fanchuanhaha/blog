#!/usr/bin/env node
/**
 * UnoCSS presetIcons 在当前构建链中无法产出 icon 样式，改为预生成 mask CSS。
 */
import { copyFileSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { encodeSvgForCss } from '@iconify/utils/lib/svg/encode-svg-for-css'
import { createNodeLoader, parseIconWithLoader } from '@unocss/preset-icons'
import { iconSafelist } from '../shared/nav-icons.mjs'

const EXTRA_ICONS = [
  'i-mdi-search',
  'i-mdi-close',
  'i-mdi-account-outline',
  'i-mdi-lock-outline',
  'i-mdi-eye-outline',
  'i-mdi-eye-off-outline',
  'i-mdi-arrow-left',
  'i-mdi-menu',
  'i-mdi-file-document-outline',
  'i-mdi-home-export-outline',
  'i-mdi-chevron-left',
  'i-mdi-chevron-down',
  'i-mdi-magnify',
  'i-mdi-refresh',
  'i-mdi-plus',
  'i-mdi-pencil-outline',
  'i-mdi-arrow-down',
  'i-mdi-arrow-up',
  'i-mdi-image-outline',
  'i-mdi-dots-vertical',
  'i-ri-arrow-left-s-line',
  'i-ri-arrow-right-s-line',
  'i-fa6-solid:bullhorn',
  'i-fa6-solid:water',
  'i-fa6-solid:anchor',
  'i-fa6-solid-angle-down',
  'i-ri-translate',
  'i-ri-calendar-2-line',
  'i-ri-file-word-line',
  'i-ri-timer-line',
  'i-ri-eye-line',
  'i-fa-search',
  'i-fa6-solid-quote-left',
  'i-fa6-solid-quote-right',
]

const icons = [...new Set([...iconSafelist, ...EXTRA_ICONS])]
const root = process.cwd()
const outFile = resolve(root, 'styles/generated-icons.css')
const publicFile = resolve(root, 'public/generated-icons.css')

function escapeClass(name) {
  return name.replace(/:/g, '\\:')
}

function encodeSvg(svg) {
  return encodeSvgForCss(svg)
}

function hasIconRules(content) {
  return /\.i-[a-z0-9-]+\\:/i.test(content) || /\.i-[a-z0-9-]+\{/i.test(content)
}

function publishCss(content) {
  writeFileSync(outFile, content)
  mkdirSync(dirname(publicFile), { recursive: true })
  writeFileSync(publicFile, content)
}

const load = await createNodeLoader()
const lines = [
  '/* Auto-generated by scripts/generate-icons-css.mjs — do not edit */',
  '.i-inline{display:inline-block;vertical-align:middle;width:1em;height:1em;flex-shrink:0;}',
]

let ok = 0
let fail = 0

for (const cls of icons) {
  const body = cls.startsWith('i-') ? cls.slice(2) : cls
  const parsed = await parseIconWithLoader(body, load, { cwd: root })
  if (!parsed?.svg) {
    console.warn(`[icons] skip: ${cls}`)
    fail++
    continue
  }

  const url = `url("data:image/svg+xml;utf8,${encodeSvg(parsed.svg)}")`
  const selector = `.${escapeClass(cls)}`
  lines.push(`${selector}{display:inline-block;width:1em;height:1em;vertical-align:middle;background-color:currentColor;-webkit-mask:${url} no-repeat;mask:${url} no-repeat;-webkit-mask-size:100% 100%;mask-size:100% 100%;}`)
  ok++
}

if (ok === 0) {
  if (existsSync(outFile)) {
    const existing = readFileSync(outFile, 'utf8')
    if (hasIconRules(existing)) {
      console.warn('[icons] no icons generated; keeping existing styles/generated-icons.css')
      copyFileSync(outFile, publicFile)
      process.exit(0)
    }
  }
  console.error('[icons] failed to generate icons and no usable fallback CSS found')
  process.exit(1)
}

const css = `${lines.join('\n')}\n`
publishCss(css)
console.log(`[icons] wrote ${ok} icons to styles/generated-icons.css and public/generated-icons.css${fail ? ` (${fail} skipped)` : ''}`)
